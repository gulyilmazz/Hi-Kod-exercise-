# Sayısal veriyi stringe çevirme
sayi = 42
sayi_str = str(sayi)

# Stringi tam sayıya çevirme
metin = "123"
metin_int = int(metin)

# Sayıyı ondalıklı hale çevirme
sayi_float = float(sayi)

# Çıktıları yazdıralım
print(type(sayi_str), sayi_str)  # <class 'str'> 42
print(type(metin_int), metin_int)  # <class 'int'> 123
print(type(sayi_float), sayi_float)  # <class 'float'> 42.0